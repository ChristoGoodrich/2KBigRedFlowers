const I18N = { zh: {}, en: {} };
Object.keys(I18N_COPY).forEach(key => {
  I18N.en[key] = I18N_COPY[key][0];
  I18N.zh[key] = I18N_COPY[key][1];
});

function normalizeI18nKey(key) {
  return String(key || '').replace(/\s+/g, ' ').trim();
}

function formatLocalized(en, zh) {
  if (!en && !zh) return '';
  return currentLang === 'zh' ? (zh || en) : (en || zh);
}

function copyPair(key) {
  const normalized = normalizeI18nKey(key);
  if (I18N_COPY[normalized]) return I18N_COPY[normalized];
  if (I18N_COPY[key]) return I18N_COPY[key];
  return null;
}

function t(key) {
  if (!key) return key;
  const pair = copyPair(key);
  return pair ? formatLocalized(pair[0], pair[1]) : key;
}

function translateTextValue(value) {
  const pair = copyPair(value);
  return pair ? formatLocalized(pair[0], pair[1]) : value;
}

function translatePlaceholderValue(value) {
  const normalized = normalizeI18nKey(value);
  const pair = I18N_PLACEHOLDERS[normalized];
  return pair ? formatLocalized(pair[0], pair[1]) : value;
}

function rememberOriginal(el, attr, value) {
  const dataName = attr === 'text' ? 'i18nOriginalText' : `i18nOriginal${attr.charAt(0).toUpperCase()}${attr.slice(1)}`;
  if (!el.dataset[dataName]) el.dataset[dataName] = normalizeI18nKey(value);
  return el.dataset[dataName];
}

function translateSimpleElement(el) {
  if (!el || el.closest('script, style')) return;
  const canFlatten = el.classList.contains('page-title') || el.classList.contains('logo-text');
  if (el.children.length > 0 && !canFlatten) return;
  const text = normalizeI18nKey(el.textContent);
  if (!text) return;
  const original = rememberOriginal(el, 'text', text);
  const translated = translateTextValue(original);
  if (translated !== original || copyPair(original)) el.textContent = translated;
}

